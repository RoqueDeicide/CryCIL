using System;
using System.Linq;

namespace CryCil
{
	/// <summary>
	/// Encapsulates 2 half-precision floating point numbers that represent coordinates in 2D space.
	/// </summary>
	public struct Vector2Half
	{
		#region Fields
		/// <summary>
		/// X-coordinate.
		/// </summary>
		public Half X;
		/// <summary>
		/// Y-coordinate.
		/// </summary>
		public Half Y;
		#endregion
	}
}